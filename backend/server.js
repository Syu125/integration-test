app.post("/run-pipeline", async (req, res) => {
  const { filename, content } = req.body;

  try {
    // STEP 1: COMPILATION CHECK (LINTING)
    // We'll use a simple regex check or try to write it to a temp file
    console.log("Stage 1: Linting...");
    io.emit("pipeline-log", "ðŸ” Linting code for syntax errors...");

    if (content.includes("import") && !content.includes("from")) {
      throw new Error("Syntax Error: Incomplete import statement.");
    }

    // STAGE 1: Security & Tests
    io.emit("pipeline-update", "ðŸ›¡ï¸ Running security scans...");
    securityCheck(content);

    io.emit("pipeline-update", "ðŸ§ª Running unit tests...");
    runUnitTests(content);

    // STAGE 2: Git Branching
    const branchName = `sync-${Date.now()}`;
    io.emit("pipeline-update", `ðŸŒ¿ Creating branch: ${branchName}...`);
    await git.checkout("main");
    await git.pull("origin", "main");

    await git.checkoutLocalBranch(branchName);

    // --- DIRECTORY GUARD ---
    // This ensures the components/ folder exists before we write the file
    const targetDir = path.join(
      __dirname,
      "../apps/course-assistant/src/components",
    );

    if (!fs.existsSync(targetDir)) {
      console.log(`ðŸ“‚ Creating missing directory: ${targetDir}`);
      io.emit("pipeline-update", "ðŸ“‚ Creating missing components directory...");
      fs.mkdirSync(targetDir, { recursive: true });
    }
    const filePath = path.join(
      __dirname,
      `../apps/course-assistant/src/components/${filename}`,
    );
    fs.writeFileSync(filePath, content);

    await git.add("./*");

    const status = await git.status();
    if (status.staged.length === 0) {
      throw new Error(
        "No changes detected. The code is identical to what is already on GitHub.",
      );
    }

    await git.commit(`New Component: ${filename}`);
    await git.push("origin", branchName);

    // STAGE 3: Open Pull Request
    io.emit("pipeline-update", "ðŸ“‚ Opening Pull Request on GitHub...");
    const pr = await octokit.pulls.create({
      owner: "Syu125",
      repo: "integration-test",
      title: `Merge New Component: ${filename}`,
      head: branchName,
      base: "main",
      body: "This PR was automatically generated by the App A Pipeline after passing security and unit tests.",
    });

    io.emit("pipeline-update", `âœ… PR Created: ${pr.data.html_url}`);

    // Switch back to main so the local repo stays clean
    await git.checkout("main");

    res.json({ prUrl: pr.data.html_url });
  } catch (err) {
    console.error(err);
    io.emit("pipeline-log", `âŒ Error: ${err.message}`);
    res.status(500).json({ error: err.message });
  }
});

const securityCheck = (code) => {
  const forbidden = [
    "eval",
    "process",
    "child_process",
    "localStorage",
    "cookie",
  ];
  for (const word of forbidden) {
    if (code.includes(word))
      throw new Error(`Security Violation: Found forbidden keyword "${word}"`);
  }
  return true;
};

// Simple unit test simulation (You can eventually plug in Vitest here)
const runUnitTests = (code) => {
  if (code.length < 10)
    throw new Error("Unit Test Failed: Component is too small/empty.");
  if (!code.includes("return"))
    throw new Error("Unit Test Failed: Component has no return statement.");
  return true;
};
